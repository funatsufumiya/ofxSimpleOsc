#include "ofApp.h"

//--------------------------------------------------------------
void ofApp::setup(){
	ofLogToConsole();
	ofSetVerticalSync(true);
	ofSetFrameRate(60);

	color = ofColor(255, 0, 0);
	background_color = ofColor(0);
	point = ofPoint(0, 0);

	w = 100.0;
	h = 100.0;

	osc_receiver.setup(7777, [&](const ofxOscMessage& msg) {
		ofxSimpleOsc::filter(msg)
			.when("/test", [&]() {
				ofLog() << "test OSC received!";
			})
			.bind("/color", &color)
			.bind("/background", &background_color)
			.bind("/point", &point)
			.bind("/size", &w, &h)
			.else_show_warning();
	});
}

